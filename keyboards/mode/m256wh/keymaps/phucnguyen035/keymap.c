#define _BASE 0
#define _GAME 1
#define _FN 2

enum custom_keycodes {
    TOGGLE_GAME = SAFE_RANGE,
};

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case TOGGLE_GAME:
            if (record->event.pressed) {
                default_layer_xor(1UL << _GAME);
            }
            return false;
        default:
            return true;
    }
}

